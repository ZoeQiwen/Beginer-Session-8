def hidup_dalam_mingggu(age):
    total_minggu= 90 * 52
    minggu_yang_sudah_dilalui = age * 52
    sisa_minggu = total_minggu - minggu_yang_sudah_dilalui
    print(f"you have {sisa_minggu} week left.")
hidup_dalam_mingggu(8)
